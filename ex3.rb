# This will print the string
puts "I will now count my chickens:"

# #{} inserts ruby into a string. This can be variables, math calculations, booleans, etc.   This will print the string and ruby code. since it's ruby, it will print math calculations inside of the #{}
puts "Hens #{25 + 30 / 6}"
puts "Roosters #{100 - 25 * 3 % 4}"

# Prints another string
puts "Now I will count the eggs:"

# Prints math calculations only
puts 3 + 2 + 1 - 5 + 4 % 2 - 1 / 4 + 6

# This will print a string with no calculations
puts "Is it true that 3 + 2 < 5 - 7?"

# This will print a boolean calculation
puts 3 + 2 < 5 - 7

# This will print a string and calculation
puts "What is 3 + 2? #{3 + 2}"
puts "What is 5 - 7? #{5 - 7}"

# Prints string only
puts "Oh, that's why it's false"

# Prints string only
puts "How about some more."

# Prints string, then boolean calc
puts "Is it greater? #{5 > -2}"
puts "Is it greater or equal? #{5 >= -2}"
puts "is it less than or equal? #{5 <= -2}"
